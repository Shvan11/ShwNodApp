CREATE TABLE "tblAlignerBatches" (
  "AlignerBatchID"            integer GENERATED BY DEFAULT AS IDENTITY,
  "AlignerSetID"              integer NOT NULL,
  "UpperAlignerCount"         integer NOT NULL,
  "LowerAlignerCount"         integer NOT NULL,
  "ManufactureDate"           date,
  "DeliveredToPatientDate"    date,
  "Notes"                     citext,
  "IsActive"                  boolean DEFAULT false,
  "BatchSequence"             integer NOT NULL,
  "UpperAlignerStartSequence" integer,
  "LowerAlignerStartSequence" integer,
  "UpperAlignerEndSequence"   integer GENERATED ALWAYS AS (
      CASE WHEN "UpperAlignerStartSequence" IS NULL THEN NULL
           ELSE ("UpperAlignerStartSequence" + "UpperAlignerCount") - 1 END) STORED,
  "LowerAlignerEndSequence"   integer GENERATED ALWAYS AS (
      CASE WHEN "LowerAlignerStartSequence" IS NULL THEN NULL
           ELSE ("LowerAlignerStartSequence" + "LowerAlignerCount") - 1 END) STORED,
  "Days"                      integer,
  "IsLast"                    boolean NOT NULL DEFAULT false,
  "CreationDate"              timestamp NOT NULL DEFAULT LOCALTIMESTAMP,
  "HasUpperTemplate"          boolean NOT NULL DEFAULT false,
  "HasLowerTemplate"          boolean NOT NULL DEFAULT false,
  "ValidityPeriod"            integer GENERATED ALWAYS AS (
      CASE WHEN "Days" IS NULL THEN NULL
           WHEN ("UpperAlignerCount" - CASE WHEN "HasUpperTemplate" THEN 1 ELSE 0 END)
              >= ("LowerAlignerCount" - CASE WHEN "HasLowerTemplate" THEN 1 ELSE 0 END)
             THEN ("UpperAlignerCount" - CASE WHEN "HasUpperTemplate" THEN 1 ELSE 0 END) * "Days"
             ELSE ("LowerAlignerCount" - CASE WHEN "HasLowerTemplate" THEN 1 ELSE 0 END) * "Days"
      END) STORED,
  "BatchExpiryDate"           date GENERATED ALWAYS AS (
      "DeliveredToPatientDate" + (
        CASE WHEN "Days" IS NULL THEN NULL
             WHEN ("UpperAlignerCount" - CASE WHEN "HasUpperTemplate" THEN 1 ELSE 0 END)
                >= ("LowerAlignerCount" - CASE WHEN "HasLowerTemplate" THEN 1 ELSE 0 END)
               THEN ("UpperAlignerCount" - CASE WHEN "HasUpperTemplate" THEN 1 ELSE 0 END) * "Days"
               ELSE ("LowerAlignerCount" - CASE WHEN "HasLowerTemplate" THEN 1 ELSE 0 END) * "Days"
        END)) STORED,
  CONSTRAINT "PK__tblAlign__1C222425A57BE68E" PRIMARY KEY ("AlignerBatchID"),
  CONSTRAINT "UQ_BatchSequence_AlignerSetID" UNIQUE ("AlignerSetID","BatchSequence"),
  CONSTRAINT "CK_AlignerBatches_Active_Requires_Delivery"
      CHECK ("IsActive" = false OR "DeliveredToPatientDate" IS NOT NULL)
);

