-- Up Migration
--
-- PostgreSQL baseline schema for Shwan Orthodontics (Phase 2 of the SQL Server -> PG migration).
-- Hand-authored from migrations/init_script.sql (live ShwanNew_Test dump, 2026-05-28).
-- Tables + types + constraints + indexes ONLY. No procs/triggers/functions/views (those move to
-- TypeScript in Phase 5; views recreated later). Identifiers are double-quoted to preserve the
-- exact SQL Server casing so kysely-codegen output matches the existing CamelCase query code.
--
-- Locked type/collation decisions (see docs/postgres-migration-plan.md):
--   * Arabic_CI_AS text  -> citext (case-insensitive =, LIKE, unique; accent-sensitive). Exception:
--     the 2-char Dolphin image-type codes stay char(2) (load-bearing fixed codes).
--   * datetime/datetime2 -> timestamp WITHOUT time zone (wall-clock; never timestamptz / useUTC:false).
--   * bit -> boolean, uniqueidentifier -> uuid, tinyint -> smallint.
--   * IDENTITY -> GENERATED BY DEFAULT AS IDENTITY (BY DEFAULT so ETL can insert explicit PKs).
--   * deterministic computed cols -> GENERATED ALWAYS ... STORED; getdate()-based ones dropped.
--   * getdate()/sysdatetime() default -> LOCALTIMESTAMP; sysutcdatetime() -> (now() AT TIME ZONE 'UTC').
--
-- Intentional deviations from a 1:1 port (flagged for review):
--   * tblInvoice was a SYSTEM_VERSIONED temporal table. System-versioning + History.tblInvoice are
--     dropped (no app consumer of the history). SysStartTime is kept as a plain created-at timestamp
--     (the app reads it as the payment time in DailyInvoicesModal); SysEndTime is dropped (unused).
--   * tblappointments.SSMA_TimeStamp (rowversion) dropped (verified unused).
--   * tblpatients.Age computed column dropped (used getdate() -> non-immutable; compute in app/view).
--   * Two CHECKs wrapping scalar fns (CK_MoreThanTotal, CK_MoreThanTotalW) and the getdate()-based
--     DOB CHECK are dropped; that logic moves to TypeScript in Phase 5.
--   * NEW unique constraints for Phase-4 ON CONFLICT upserts: tblsms(date), tblTimePointImages(TimePointID,ImageType).
--   * Redundant indexes that merely duplicate a PK / a sibling index are not recreated.

