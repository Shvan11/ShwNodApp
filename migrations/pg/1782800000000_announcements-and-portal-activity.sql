-- Announcements (aligner portal doctors) + Portal Activity feed (staff header bell).
--
-- Three pieces, three sync postures (docs/sync-cdc.md):
--
--   1. doctor_announcements — staff-app-authored, FORWARD-ONLY local → Supabase
--      (lab_cases pattern: cdc_capture('announcement_id','failover') trigger here,
--      none on the mirror; deliberately NO updated_at so the table stays OUT of
--      reverse sync; plain identity — the mirror never inserts announcements).
--      The portal reads it on the mirror under RLS (broadcast OR own dr_id —
--      aligner-portal-external/sql/phase3-announcements.sql).
--
--   2. doctor_announcement_reads — PORTAL-written on the mirror, REVERSE-synced home
--      (aligner_notes pattern): updated_at + odd/even identity split (local ODD
--      START 1 INCREMENT 2, mirror EVEN START 2 INCREMENT 2 — inserts never collide)
--      + set_updated_at()/cdc_capture() triggers. Both fns skip under
--      app.cdc_origin='reverse', so reverse-applied portal rows neither echo back
--      nor get their updated_at re-stamped (the LWW keystone).
--
--   3. aligner_activity_flags — generalized into the staff Portal-Activity feed:
--      ck_activitytype widened (+PhotoUploaded, +FileUploaded) and a `source`
--      column ('staff'|'portal', default 'staff') added. The staff bell reads only
--      source='portal' rows; existing staff-side flag writes stay 'staff' via the
--      default and never surface in the feed.
--
-- Apply (squashed-baseline state — `node-pg-migrate up` would replay the baseline;
-- run directly instead). Apply the Supabase mirror FIRST (CDC replicates row DATA,
-- never DDL — the mirror must have the tables/column before the first forward
-- upsert), then the portal RLS/grants, then LOCAL:
--   scripts/psql.sh supa  -f migrations/supabase/announcements-2026-07-11.sql
--   scripts/psql.sh supa  -f aligner-portal-external/sql/phase3-announcements.sql
--   scripts/psql.sh local -f migrations/pg/1782800000000_announcements-and-portal-activity.sql
-- Then regenerate types: npm run db:codegen
-- This file is the node-pg-migrate record.

