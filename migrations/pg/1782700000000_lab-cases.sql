-- Lab case tracker: a case header (`lab_cases`, one per prosthetic work item) + an
-- append-only event log (`lab_case_events`) recording every stage transition,
-- remake, hold/resume, note, and cancel. Tracks where a Crown/Bridge (work type 17)
-- or Veneers (work type 9) case physically sits between the clinic and the external
-- dental lab — see docs plan "Lab Tracking Feature".
--
-- Sync posture: FAILOVER-MIRRORED, forward-only (local -> Supabase) — clones the
-- shade-tables precedent (migrations/pg/1782300000000_shade-tables.sql). Both tables
-- carry a `cdc_capture('id', 'failover')` trigger; the Supabase mirror
-- (migrations/supabase/lab-cases-2026-07-02.sql) has the identical DDL minus the
-- trigger (a forward-only TARGET never captures back). Deliberately NO `updated_at`
-- column on either table: failover captures all INSERT/UPDATE/DELETE regardless, and
-- the absence of `updated_at` keeps both tables OUT of two-way reverse sync (the
-- reverse discovery query only enrolls tables that HAVE `updated_at`) — correct,
-- since the staff app is the sole writer and `lab_case_events` is append-only so
-- whole-row LWW would be meaningless. `id` is plain `GENERATED BY DEFAULT AS
-- IDENTITY` (not the reverse-set ODD/EVEN split) since these tables never receive
-- reverse inserts. No one-time row load — both tables are born empty.
--
-- Apply (squashed-baseline state — `node-pg-migrate up` would replay the baseline;
-- run directly instead). Apply the Supabase mirror FIRST, then LOCAL:
--   scripts/psql.sh supa  -f migrations/supabase/lab-cases-2026-07-02.sql
--   scripts/psql.sh local -f migrations/pg/1782700000000_lab-cases.sql
-- Then regenerate types: npm run db:codegen
-- This file is the node-pg-migrate record.

